#include "../cub3d.h"

int	cub_count_shift(void)
{
	int shift;
	int	map_block;

	map_block = BLOCK;
	shift = 0;
	while (1)
	{
		if ((map_block >> shift) == 1)
			return (shift);
		shift++;
	}
}

